/*==============================================================================
 *  MAIN
 *==============================================================================*/
void main(void)
{
    InitSysCtrl();
    InitGpio();
    DINT;
    InitPieCtrl();
    IER = 0x0000; IFR = 0x0000;
    InitPieVectTable();
    EINT; ERTM;

    init_sci();

    memset((void*)&pq_result, 0, sizeof(pq_result));
    pq_result.event = EVT_NORMAL;

    sci_puts("\r\n=== PQ Monitor: Real-Time Measurement ===\r\n");

    /*── Process data array cycle by cycle (mimics real-time ADC capture) ──*/
    float sum_v2 = 0.0f, sum_i2 = 0.0f, sum_vi = 0.0f;
    float thd_v_accum = 0.0f, thd_i_accum = 0.0f;   /* FIX: per-cycle THD */
    uint16_t cyc;

    for (cyc = 0u; cyc < NUM_CYCLES; cyc++)
    {
        uint16_t k;
        uint8_t  h;
        uint16_t base = cyc * SPC;
        for (k = 0u; k < SPC; k++)
        {
            v_cyc[k] = v_data[base + k];
            i_cyc[k] = i_data[base + k];
        }

        /*── Per-cycle Vrms ───────────────────────────────────────────────*/
        float cyc_v2 = 0.0f;
        for (k = 0u; k < SPC; k++) cyc_v2 += v_cyc[k] * v_cyc[k];
        vrms_per_cycle[cyc] = sqrtf(cyc_v2 / (float)SPC);

        /*── Accumulate global PQ sums ────────────────────────────────────*/
        compute_cycle_pq(cyc, &sum_v2, &sum_i2, &sum_vi);

        /*── FIX: Per-cycle THD accumulation — averages across all cycles ─*/
        compute_fft(v_cyc, SPC);
        {
            float V1  = fft_mag[1];
            float Vh2 = 0.0f;
            for (h = 2u; h <= 10u; h++) Vh2 += fft_mag[h] * fft_mag[h];
            if (V1 > 0.01f) thd_v_accum += sqrtf(Vh2) / V1 * 100.0f;
        }
        compute_fft(i_cyc, SPC);
        {
            float I1  = fft_mag[1];
            float Ih2 = 0.0f;
            for (h = 2u; h <= 10u; h++) Ih2 += fft_mag[h] * fft_mag[h];
            if (I1 > 0.01f) thd_i_accum += sqrtf(Ih2) / I1 * 100.0f;
        }
    }

    /*── Finalize PQ parameters ───────────────────────────────────────────*/
    finalize_pq(NUM_CYCLES, sum_v2, sum_i2, sum_vi, thd_v_accum, thd_i_accum);

    /*── Event detection ──────────────────────────────────────────────────*/
    detect_events();

    /*── DWT transient detection ──────────────────────────────────────────*/
    compute_dwt_level1();

    /*── FIX 2: Send extended 6-line frame to Python dashboard ───────────*/
    send_csv_full();

    sci_puts("\r\n[DONE] Results in Watch Window.\r\n");

    /*── Repeat frame every ~4 seconds so pq_receiver_v2.py gets live data ─*/
    for (;;)
    {
        uint32_t delay;
        for (delay = 0u; delay < 3000000UL; delay++) { __asm(" NOP"); }
        send_csv_full();
    }
}

/*==============================================================================
 *  DWT LEVEL-1 — Db4 detail energy per cycle
 *==============================================================================*/
static void compute_dwt_level1(void)
{
    uint16_t cyc, k;
    float detail, energy;

    for (cyc = 0u; cyc < NUM_CYCLES; cyc++)
    {
        uint16_t base = cyc * SPC;
        energy = 0.0f;
        for (k = 0u; k + 3u < SPC; k += 2u)
        {
            detail = DB4_G0*v_data[base+k]   + DB4_G1*v_data[base+k+1]
                   + DB4_G2*v_data[base+k+2] + DB4_G3*v_data[base+k+3];
            energy += detail * detail;
        }
        dwt_detail_energy[cyc] = energy;
    }
}

/*==============================================================================
 *  REAL-INPUT DFT — O(N²), N=40
 *  Bin k → frequency k × 50 Hz
 *==============================================================================*/
static void compute_fft(const volatile float *buf, uint16_t N)
{
    uint16_t k, n;
    for (k = 0u; k <= N/2u; k++)
    {
        float re = 0.0f, im = 0.0f;
        for (n = 0u; n < N; n++)
        {
            float angle = 2.0f * PI_F * (float)k * (float)n / (float)N;
            re += buf[n] * cosf(angle);
            im -= buf[n] * sinf(angle);
        }
        fft_mag[k] = (k == 0u)
                     ? (re / (float)N)
                     : (2.0f * sqrtf(re*re + im*im) / (float)N);
    }
}

/*==============================================================================
 *  COMPUTE CYCLE PQ
 *==============================================================================*/
static void compute_cycle_pq(uint16_t cycle_idx,
                               float *sum_v2, float *sum_i2, float *sum_vi)
{
    uint16_t k;
    for (k = 0u; k < SPC; k++)
    {
        *sum_v2 += v_cyc[k] * v_cyc[k];
        *sum_i2 += i_cyc[k] * i_cyc[k];
        *sum_vi += v_cyc[k] * i_cyc[k];
    }
    (void)cycle_idx;
}

/*==============================================================================
 *  FINALIZE PQ — compute all parameters after all cycles processed
 *==============================================================================*/
static void finalize_pq(uint16_t n_cyc,
                          float sum_v2, float sum_i2, float sum_vi,
                          float thd_v_accum, float thd_i_accum)
{
    float N = (float)((uint32_t)n_cyc * SPC);
    uint16_t n;

    pq_result.Vrms = sqrtf(sum_v2 / N);
    pq_result.Irms = sqrtf(sum_i2 / N);
    pq_result.P    = sum_vi / N;
    pq_result.S    = pq_result.Vrms * pq_result.Irms;

    float q2 = pq_result.S * pq_result.S - pq_result.P * pq_result.P;
    pq_result.Q  = (q2 > 0.0f) ? sqrtf(q2) : 0.0f;

    /* True Power Factor = P / S */
    pq_result.PF = (pq_result.S > 0.001f) ? (pq_result.P / pq_result.S) : 0.0f;

    pq_result.freq = (float)FS / (float)SPC;   /* 50 Hz */

    /* FIX: THD = average of per-cycle THD accumulated in main loop.
     * Must be computed BEFORE DF so THD_I is valid when DF is calculated.  */
    pq_result.THD_V = thd_v_accum / (float)n_cyc;
    pq_result.THD_I = thd_i_accum / (float)n_cyc;

    /* ── Distortion Factor = 1 / sqrt(1 + THD_I²)
     *    Accounts for harmonic distortion in the current waveform.
     *    THD_I is in percent, so divide by 100 first.                       */
    {
        float thd_i_pu = pq_result.THD_I / 100.0f;   /* per-unit */
        pq_result.DF = 1.0f / sqrtf(1.0f + thd_i_pu * thd_i_pu);
    }

    /* ── Displacement Factor = cos(phi1)
     *    phi1 = phase angle between V and I fundamentals (50 Hz, bin 1).
     *    Computed by evaluating a single DFT bin over the full 2000-sample
     *    record — no extra arrays needed, reuses loop variable n.           */
    {
        float v_cos = 0.0f, v_sin = 0.0f;
        float i_cos = 0.0f, i_sin = 0.0f;
        float angle;
        for (n = 0u; n < NUM_SAMPLES; n++)
        {
            /* angle = 2*pi*F0*n/FS = 2*pi*50*n/2000 = 2*pi*n/40 */
            angle  = 2.0f * PI_F * (float)n / (float)SPC;
            v_cos += v_data[n] * cosf(angle);
            v_sin += v_data[n] * sinf(angle);
            i_cos += i_data[n] * cosf(angle);
            i_sin += i_data[n] * sinf(angle);
        }
        float v_phase = atan2f(v_sin, v_cos);   /* phase of V fundamental   */
        float i_phase = atan2f(i_sin, i_cos);   /* phase of I fundamental   */
        float phi1    = v_phase - i_phase;       /* displacement angle       */
        pq_result.DPF = cosf(phi1);             /* Displacement Factor      */
    }

    /* FIX: FFT spectrum — use a normal (non-sag) cycle so Python plot shows
     * actual harmonic content, not the attenuated sag-cycle spectrum.
     * Cycle 0 is always a normal-amplitude cycle.                           */
    {
        uint16_t k;
        for (k = 0u; k < SPC; k++)
        {
            v_cyc[k] = v_data[k];   /* cycle 0 */
            i_cyc[k] = i_data[k];
        }
    }
    /* Leave current-channel FFT spectrum in fft_mag[] for send_csv_full()  */
    compute_fft(i_cyc, SPC);
}

/*==============================================================================
 *  EVENT DETECTION
 *==============================================================================*/
static void detect_events(void)
{
    uint16_t cyc;
    uint16_t sag_cnt = 0u, swell_cnt = 0u;
    uint16_t first_event = 0u, last_event = 0u;
    uint8_t  found = 0u;

    for (cyc = 0u; cyc < NUM_CYCLES; cyc++)
    {
        float ratio = vrms_per_cycle[cyc] / VNOM;
        if (ratio < SAG_THRESH)
        {
            sag_cnt++;
            if (!found) { first_event = cyc + 1u; found = 1u; }
            last_event = cyc + 1u;
        }
        else if (ratio > SWELL_THRESH)
        {
            swell_cnt++;
            if (!found) { first_event = cyc + 1u; found = 1u; }
            last_event = cyc + 1u;
        }
    }

    if      (sag_cnt   > 0u) pq_result.event = EVT_SAG;
    else if (swell_cnt > 0u) pq_result.event = EVT_SWELL;
    else                      pq_result.event = EVT_NORMAL;

    pq_result.event_start_cycle = first_event;
    pq_result.event_end_cycle   = last_event;
}

