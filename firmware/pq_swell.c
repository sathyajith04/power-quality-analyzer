static void sci_putchar(char c)
{
    while (SciaRegs.SCICTL2.bit.TXRDY == 0) {}
    SciaRegs.SCITXBUF = (uint16_t)c;
}

static void sci_puts(const char *s)
{ while (*s) sci_putchar(*s++); }

static void sci_putfloat(float val, uint8_t dec)
{
    if (val < 0.0f) { sci_putchar('-'); val = -val; }
    uint32_t ip = (uint32_t)val;
    sci_putuint(ip);
    if (dec > 0u)
    {
        sci_putchar('.');
        val -= (float)ip;
        uint8_t d;
        for (d = 0u; d < dec; d++)
        {
            val *= 10.0f;
            uint8_t digit = (uint8_t)val;
            sci_putchar('0' + digit);
            val -= (float)digit;
        }
    }
}

static void sci_putuint(uint32_t val)
{
    char buf[10]; int i = 0;
    if (val == 0u) { sci_putchar('0'); return; }
    while (val > 0u) { buf[i++] = (char)('0' + val % 10u); val /= 10u; }
    while (--i >= 0) sci_putchar(buf[i]);
}

static const char *event_name(Event_t e)
{
    return (e == EVT_SAG) ? "SAG" : (e == EVT_SWELL) ? "SWELL" : "NORMAL";
}

/*==============================================================================
 *  FIX 2: EXTENDED 6-LINE UART FRAME for pq_receiver_v2.py
 *
 *  Frame layout (receiver expects this exact prefix per line):
 *    CSV,Vrms,Irms,THD_V%,THD_I%,P,PF,DF,DPF,EVENT
 *    VWAVE,s0,...,s1999
 *    IWAVE,s0,...,s1999
 *    VCYC,c0,...,c49
 *    DWT,e0,...,e49
 *    FFT,m0,...,m20
 *
 *  NOTE: At 115200 baud (~11520 B/s) the two 2000-sample lines (~16 KB each
 *  at 3 dp) take roughly 3–4 s. Use --wait 10 when launching Python:
 *      python pq_receiver_v2.py --port COM4 --wait 10
 *==============================================================================*/
static void send_csv_full(void)
{
    uint16_t k;

    /* Line 1 — scalar metrics */
    sci_puts("# CSV,Vrms,Irms,THD_V%,THD_I%,P,PF,DF,DPF,EVENT\r\n");
    sci_puts("CSV,");
    sci_putfloat(pq_result.Vrms,  3); sci_putchar(',');
    sci_putfloat(pq_result.Irms,  4); sci_putchar(',');
    sci_putfloat(pq_result.THD_V, 2); sci_putchar(',');
    sci_putfloat(pq_result.THD_I, 2); sci_putchar(',');
    sci_putfloat(pq_result.P,     2); sci_putchar(',');
    sci_putfloat(pq_result.PF,    4); sci_putchar(',');
    sci_putfloat(pq_result.DF,    4); sci_putchar(',');
    sci_putfloat(pq_result.DPF,   4); sci_putchar(',');
    sci_puts(event_name(pq_result.event));
    sci_puts("\r\n");

    /* Line 2 — all 2000 voltage samples */
    sci_puts("VWAVE");
    for (k = 0u; k < NUM_SAMPLES; k++)
    {
        sci_putchar(',');
        sci_putfloat(v_data[k], 3);
    }
    sci_puts("\r\n");

    /* Line 3 — all 2000 current samples */
    sci_puts("IWAVE");
    for (k = 0u; k < NUM_SAMPLES; k++)
    {
        sci_putchar(',');
        sci_putfloat(i_data[k], 4);
    }
    sci_puts("\r\n");

    /* Line 4 — 50 per-cycle Vrms values */
    sci_puts("VCYC");
    for (k = 0u; k < NUM_CYCLES; k++)
    {
        sci_putchar(',');
        sci_putfloat(vrms_per_cycle[k], 3);
    }
    sci_puts("\r\n");

    /* Line 5 — 50 DWT detail energies */
    sci_puts("DWT");
    for (k = 0u; k < NUM_CYCLES; k++)
    {
        sci_putchar(',');
        sci_putfloat(dwt_detail_energy[k], 2);
    }
    sci_puts("\r\n");

    /* Line 6 — 21 FFT magnitude bins (current spectrum) */
    sci_puts("FFT");
    for (k = 0u; k <= SPC/2u; k++)
    {
        sci_putchar(',');
        sci_putfloat(fft_mag[k], 4);
    }
    sci_puts("\r\n");
}

