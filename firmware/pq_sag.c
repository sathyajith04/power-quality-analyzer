/*==============================================================================
 *  MAIN
 *==============================================================================*/
void main(void)
{
    InitSysCtrl();
    InitGpio();
    DINT;
    InitPieCtrl();
    IER = 0x0000; IFR = 0x0000;
    InitPieVectTable();
    EINT; ERTM;

    init_sci();

    memset((void*)&pq_result, 0, sizeof(pq_result));
    pq_result.event = EVT_NORMAL;

    sci_puts("\r\n=== PQ Monitor: Real-Time Measurement ===\r\n");

    /*── Process data array cycle by cycle (mimics real-time ADC capture) ──*/
    float sum_v2 = 0.0f, sum_i2 = 0.0f, sum_vi = 0.0f;
    float thd_v_accum = 0.0f, thd_i_accum = 0.0f;   /* FIX: per-cycle THD */
    uint16_t cyc;

    for (cyc = 0u; cyc < NUM_CYCLES; cyc++)
    {
        uint16_t k;
        uint8_t  h;
        uint16_t base = cyc * SPC;
        for (k = 0u; k < SPC; k++)
        {
            v_cyc[k] = v_data[base + k];
            i_cyc[k] = i_data[base + k];
        }

        /*── Per-cycle Vrms ───────────────────────────────────────────────*/
        float cyc_v2 = 0.0f;
        for (k = 0u; k < SPC; k++) cyc_v2 += v_cyc[k] * v_cyc[k];
        vrms_per_cycle[cyc] = sqrtf(cyc_v2 / (float)SPC);

        /*── Accumulate global PQ sums ────────────────────────────────────*/
        compute_cycle_pq(cyc, &sum_v2, &sum_i2, &sum_vi);

        /*── FIX: Per-cycle THD accumulation — averages across all cycles ─*/
        compute_fft(v_cyc, SPC);
        {
            float V1  = fft_mag[1];
            float Vh2 = 0.0f;
            for (h = 2u; h <= 10u; h++) Vh2 += fft_mag[h] * fft_mag[h];
            if (V1 > 0.01f) thd_v_accum += sqrtf(Vh2) / V1 * 100.0f;
        }
        compute_fft(i_cyc, SPC);
        {
            float I1  = fft_mag[1];
            float Ih2 = 0.0f;
            for (h = 2u; h <= 10u; h++) Ih2 += fft_mag[h] * fft_mag[h];
            if (I1 > 0.01f) thd_i_accum += sqrtf(Ih2) / I1 * 100.0f;
        }
    }

    /*── Finalize PQ parameters ───────────────────────────────────────────*/
    finalize_pq(NUM_CYCLES, sum_v2, sum_i2, sum_vi, thd_v_accum, thd_i_accum);

    /*── Event detection ──────────────────────────────────────────────────*/
    detect_events();

    /*── DWT transient detection ──────────────────────────────────────────*/
    compute_dwt_level1();

    /*── FIX 2: Send extended 6-line frame to Python dashboard ───────────*/
    send_csv_full();

    sci_puts("\r\n[DONE] Results in Watch Window.\r\n");

    /*── Repeat frame every ~4 seconds so pq_receiver_v2.py gets live data ─*/
    for (;;)
    {
        uint32_t delay;
        for (delay = 0u; delay < 3000000UL; delay++) { __asm(" NOP"); }
        send_csv_full();
    }
}

